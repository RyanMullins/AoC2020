fn count_collisions (forest:Vec<Vec<char>>, movr: usize, movd: usize) -> u32 {
    const TREE: char = '#';

    let mut count: u32 = 0;
    let mut col: usize = 0;
    let mut row: usize = 0;

    for tree_line in &forest {
        if row % movd == 0 {
            let tree_index = col % tree_line.len();
            
            if tree_line[tree_index] == TREE {
                count = count + 1;
            }
    
            col = col + movr;
        }

        row = row + 1;
    }

    count 
}

